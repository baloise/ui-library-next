export const isEnterKey = (event: KeyboardEvent) => event.key === 'Enter'

export const isEscapeKey = (event: KeyboardEvent) => event.key === 'Escape'

export const isArrowDownKey = (event: KeyboardEvent) => event.key === 'ArrowDown' || event.key === 'Down'

export const isArrowUpKey = (event: KeyboardEvent) => event.key === 'ArrowUp' || event.key === 'Up'
